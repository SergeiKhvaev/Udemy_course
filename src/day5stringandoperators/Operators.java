package day5stringandoperators;

public class Operators {
    public static void main(String[] args) {

        int num = 1 + 2; // = - assigning operator, which use to assign value to variable, + is plus (addition) operator

        double rem = 4 % 3; // % - remainder operator - рузультат покажет остаток после деления 4 на 3 = 1
        System.out.println(rem);
        double rem2 = 20 % 3; // reminder = 2
        System.out.println(rem2);
    }
}
