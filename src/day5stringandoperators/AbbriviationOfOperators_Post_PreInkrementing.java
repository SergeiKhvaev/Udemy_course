package day5stringandoperators;

public class AbbriviationOfOperators_Post_PreInkrementing {
    public static void main(String[] args) {
        int result = 1;
        result++; // postincrementing or abriviation of operators
        System.out.println(result); // 2

        result--;// postdecrementing or abriviation of operators
        System.out.println(result); // 1

        //result = result +2; // we also can abbriviate that statment
        result+=2;// 3
        System.out.println(result);


    }
}
